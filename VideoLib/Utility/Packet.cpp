#include "Packet.hpp"

using vl::Packet;

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
Packet::Packet()
: mPacket()
{
  av_init_packet(&mPacket);

  mPacket.data = nullptr;

  mPacket.size = 0;
}

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
Packet::~Packet()
{
  av_packet_unref(&mPacket);
}
